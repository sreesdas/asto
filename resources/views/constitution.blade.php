    <h3 class="text-center text-dark fw-bold mb-5">Constitution & Byelaws of Association of Scientific and Technical Officers 2019</h3>

    <h5 class="text-info mb-3">1.0 PREAMBLE</h5>
    <p>We the officers of the Oil and Natural Gas Corporation Limited having unanimously resolved to form an Association with aims and objectives mentioned below do hereby adopt and give to ourselves the following constitution:</p>

    <h5 class="text-info mb-3">2.0 NAME OF THE ASSOCIATION:</h5>
    <p>The Association of Scientific and Technical Officers, abbreviated as ‘ASTO’.</p>

    <h5 class="text-info mb-3">3.0 EMBLEM:</h5>
    <p>The emblem duly ratified by the General Body in its meeting held on 27th September 1969, is shown on the top left corner. It is in the shape of an oil drop with crude oil colour backdrop. At the bottom is written ‘प्राप्य वरान्नि बोधत’. The motto at the bottom reads and means ‘Stop Not Till the Goal Is Achieved’. In all functions and correspondence related to the Association, this shall be used.</p>

    <h5 class="text-info mb-3">4.0 Aims and Objectives:</h5>
    <p>The association shall function to :-

        <br><br> 4.1 Safeguard and promote the individual and collective interest of the officers of the Oil and Natural Gas Corporation Limited (ONGC) and ONGC group of Companies.
        
        <br><br> 4.2 Promote the scientific, technical, managerial, social and welfare activities of the officers.
        
        <br><br> 4.3 Suggest ways and means to increase and maximize production and productivity of the ONGC in the best interest of the organization and country and to adopt measures to get the suggestions implemented.
        
        <br><br> 4.4 To act for safeguarding the interest of the Oil and Natural Gas Corporation Limited (ONGC) and ONGC group of Companies.
        
        <br><br> 4.5 To organize and unite all officers of the Oil and Natural Gas Corporation Limited (ONGC) and ONGC group of Companies and to regulate their relations with their employers.
        
        <br><br> 4.6 To act as a liaison between officers and management of the of the Oil and Natural Gas Corporation Limited (ONGC) and ONGC group of Companies.
        
        <br><br> 4.7 To continue to render loyal and efficient service to the Nation, Oil and Natural Gas Corporation Limited (ONGC) and ONGC group of Companies.
        
        <br><br> 4.8 To make legal/financial aid available to all the members in cases related to ASTO.
        
        <br><br> 4.9 To seek redressal of grievances of the members through representation before appropriate authority.
        
        <br><br> 4.10 To promote, collect funds from members to extend help to the nation for natural calamities/ national disaster.
        
        <br><br> 4.11 To work for the promotion of professional competence, integrity, application of talents-individually and collectively to bring out orderly changes in perspective, organisation and functioning amongst member for the growth and progress of the organisation and its employees.
        
        <br><br> 4.12 To do all things necessary or expedient and to do such other act or acts as are incidental or conducive to the attainment of the above aims and objectives. </p>

    <h5 class="text-info">5.0 MEMBERSHIP:</h5>
    <p> <br> 5.1 The Membership of the Association will be open to all the below Board level officers of the Oil and Natural Gas Corporation Limited (ONGC) and ONGC group of Companies on payment of prevailing membership subscription except those appointed on contract or on advisory capacity.

    <br><br> 5.2 Memberships will be automatically terminated :

    <br><br> 5.2.1 Upon resignation/separation of the member from organization.

    <br><br> 5.2.2 Any member whose subscription has not been paid for 3 months consecutively from the date when subscription falls due shall ipso facto cease to be a member of the Association.

    <br><br> 5.2.3 Notwithstanding what is stated in clause <br><br> 5.2.1 above in the event of arbitrary separation of a member from services of the corporation, the local unit /CEC/CWC may continue the membership, from their member, until such time it is decided that separation was just and fair.

    <br><br> 5.2.4 Members whose membership is discontinued as per provision of clause <br><br> 5.2.2 can re-join the association by paying the monthly subscription.

    <br><br> 5.3 A member shall be expelled temporarily or permanently from the association and his name removed from the list of members, if such member has :

    a) Conducted himself in a manner which is unbecoming to a member of the associations,
    or

    b) Intentionally done act of omission liable to injure the credit and good name of association
    or

    c) Acted against the interest of the association which includes financial irregularities, sabotaging, the common interest of general member etc.

    <br><br> 5.4 Any member of the Association, other than CEC member, is found acting in a manner prejudicial or detrimental to the interest of the officers’ community, the local unit will have the authority to issue a show-cause notice giving reasonable time for explanation. The local unit will have the authority to debar, expel or suspend the membership of such an officer, if satisfactory explanation is not submitted within the time limit provided. Post facto approval shall be obtained from the CEC.

    <br><br> 5.5 Register of Members:
    The association shall keep a register at unit level of all its members containing their Name and CPF number in digital form or hard copy. </p>

